<h3>What is account recovery?</h3>
<p>Account recovery means (re)configuring your passbolt plugin to be used with an existing account. It will only work if you have previously created an account on passbolt and made a backup of your private key.</p>
<p>You may need to recover your account you are trying to use passbolt from a new device, if your system was formatted, or maybe if you
uninstalled and reinstalled your browser.</p>

